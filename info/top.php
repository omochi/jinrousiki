<h2>新規実装 / 仕様変更</h2>
<ul>
  <li>出現率変動モード：「新顔村」「平等村」オプション実装</li>
  <li>鬼火：占い師に占われると鬼判定が出る。</li>
  <li>天火：占い師に占われると人狼判定が出る。</li>
  <li>狐火：占い師に占われると呪殺される。</li>
  <li>古戦場火：占い師に占われると蝙蝠判定が出る。</li>
  <li>松明丸：ひよこ鑑定士に占われると蝙蝠判定が出る。</li>
  <li>火狼：一度だけ、襲撃に成功した人を噛み殺す代わりに天火を付加する人狼。</li>
  <li>宙狐：一度だけ、人狼に襲撃されたら襲撃してきた人狼に狐火を付加する妖狐。</li>
</ul>

