<h1>開発状況</h1>
<h2>新規実装 / 仕様変更</h2>
<ul>
  <li>求道者：コピー先の基本種に変化する特殊な神話マニア</li>
  <li>元求道者：コピー後の求道者に付加される</li>
  <li>山わろ：劣化強毒者相当の毒を持つ特殊な復讐者</li>
  <li>麒麟：逃亡先が人狼系・暗殺者系・鬼陣営だった場合は死亡する特殊な逃亡者</li>
  <li>身代わり地蔵：受援者に庇護者を付加する上位後援者</li>
  <li>がしゃどくろ：呪いを持った特殊な復讐者。処刑投票先が人外なら死の宣告を付加する</li>
  <li>狂骨：釣瓶落とし相当の能力を持つ特殊な復讐者</li>
  <li>夜刀神：人狼に襲撃されて死亡した場合、一度だけ即座に蘇生する特殊な復讐者</li>
</ul>

