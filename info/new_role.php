○騎士 (陣営：村人、占い結果：村人、霊能結果：村人、巫女結果：村人)
いわゆる毒＋狩人ですが、吊られた場合は毒は発動しません。

※Ver. 1.4.0 alpha8〜
闇鍋村では20人未満では出現しません。
