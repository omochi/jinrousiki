○出題者 (陣営：村人、占い結果：村人、霊能結果：村人、巫女結果：出題者)
クイズ村の GM です。闇鍋村にも低確率で出現します。
勝利条件は「GM 以外が全滅していること」。
ルールの特殊なクイズ村以外ではまず勝ち目はありません。
引いたら諦めてください。

○不審者 (陣営：村人、占い結果：人狼、霊能結果：村人、巫女結果：村人)
